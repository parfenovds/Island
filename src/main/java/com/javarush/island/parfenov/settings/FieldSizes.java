package com.javarush.island.parfenov.settings;

public class FieldSizes {
    private final int width;
    private final int height;

    public FieldSizes(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
